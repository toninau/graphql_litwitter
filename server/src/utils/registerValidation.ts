import { UsernamePasswordInput, FieldError } from '../resolvers/userResolver';

export const registerValidation = (options: UsernamePasswordInput): FieldError[] | null => {
  if (options.username.length <= 2) {
    return [
      {
        field: 'username',
        message: 'length must be greater than 2',
      },
    ];
  }

  if (options.password.length <= 2) {
    return [
      {
        field: 'password',
        message: 'length must be greater than 2',
      },
    ];
  }

  return null;

};